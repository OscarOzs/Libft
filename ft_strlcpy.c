#include <string.h>

size_t	ft_strlcpy(char *dst, const char *src, size_t size)
{
	int i;
	int j;

	i = 0;
	while (dst[i] != '\0')
		i++;
	j = 0;
	while (src[j] != '\0' && j < size)
	{
		dst[i] = src[j];
		i++;
		j++;
	}
	dst[i] = '\0';
	return (i);
}
